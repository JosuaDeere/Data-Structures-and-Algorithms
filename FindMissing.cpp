int findMissing(list<int> in_List)
{
    //variables that will be compared to see the differences
    int ActualDiff = 0;
    int NewDiff = 0;
    int result = 0;
    //iterator to my list
    list<int>::iterator it;
    it = in_List.begin();
    //init the actual diff, if any other is differente from this, it means there is the missing number, unless actual diff is just bigger than newsiff, then it means the missing number is at the beginning
    ActualDiff = *++it - *in_List.begin();
   
    for (; it != in_List.end();it++)
    {
        
        //temp to save value
        auto temp = *it;
        //new diff to be compared
        NewDiff = *(++it) - temp;
        //retunr to previous element
        it--;
        if (NewDiff > ActualDiff)
        {
            // if we work with negative numbers then instead of add we substract, otherwise add.
            result = NewDiff < 0 ? *it -NewDiff : *it + ActualDiff;
            return result;
        }
        else if (NewDiff < ActualDiff)
        {
            //if we work with negtive numbers add instead of subtract, otherwise substract
            result= NewDiff < 0 ? *it + ActualDiff :*it - NewDiff;
            return result;
        }
    }
    return 0;
}